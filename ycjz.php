          <p>
            <span style="font-size: 18px;">
              <strong>怀孕后能整牙吗?</strong>
              牙齿不齐在人群中很常见，如果想要牙齿回归整齐，则可以通过牙齿矫正使牙列回归整齐。不过，孕妇是一个特殊的群体，在做某些治疗时都要考虑一个问题——会不会影响到胎儿?牙齿矫正也是一样。那么怀孕了能进行牙齿矫正吗?
            </span>
          </p>
          <p style="text-align: center;">
            <span style="font-size: 18px;">
              <img src="./img/ycjzImg/1.jpg" alt="牙齿矫正" style="width:600px;height:387px;">
            </span>
          </p>
          <p>
            <span style="font-size: 18px;">
                <strong>&nbsp; &nbsp;怀孕后能整牙吗?</strong>
              其实，怀孕并不属于牙齿矫正禁忌范围，在原则上，可以在怀孕期间进行正畸，但如果你能提前计划，它是在此期间建议，以避免怀孕，然后正畸，会更好矫正。
            </span>
          </p>
          <p>
            <span style="font-size: 18px;">
              &nbsp; &nbsp;首先，矫治器可能增大妊娠反应：怀孕了以后，由于妊娠不良反应过程中容易出现恶心。在口中的矫正器，刷牙，牙齿矫正用橡皮筋等，这些都可能刺激孕妇，导致妊娠呕吐。
            </span>
          </p>
          <p>
            <span style="font-size: 18px;">
              &nbsp; &nbsp;第二，发生牙齿疾病处理会更加麻烦：怀孕之后体内内分泌发生变化，牙龈肿以及牙龈组织增生的烦恼随之而现，所以要维护好口腔医疗卫生，以免由于没有牙齿刷不干净之后出现的牙垢牙石等等的积存加重牙齿口腔疾病、龋齿的发生。
              而且在怀孕期间，很多药物不能使用，牙齿疾病会让人头秃。好好保持口腔卫生~
            </span>
          </p>
          <p>
            <span style="font-size: 18px;">
            &nbsp; &nbsp;同时，需要在X射线前面，拔牙等时不适合在这个问题上新安装的牙套，而且对孕妇的额外负担进行校正，所以虽然怀孕是不是一个禁忌牙齿矫正，但它不建议怀孕牙齿校正后开始。
            </span>
          </p>
          <p>
            <span style="font-size: 18px;">
              &nbsp; &nbsp;简而言之，整牙可以怀孕，可怀孕了不建议整牙!
